def add(a,b):
    num1 = a
    num2 = b 
    return num1 + num2

print(add(2,2))

def sub(a,b):
    num1 = a
    num2 = b 
    return num1 - num2

print(sub(8,2))


def mul(a,b):
    num1 = a
    num2 = b 
    return num1 * num2

print(mul(2,9))

def square(x):
    return x*x
print(square(9))